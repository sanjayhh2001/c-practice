/*
Write a C program to:

Declare one integer, one float, and one character variable

Assign values to them

Print all values on separate lines
*/


#include<stdio.h>

int main()
{
    int a = 1;
    float b = 3.145;
    char ch = 's';


    printf("integer is: %d\n",a);
    printf("float is: %f\n",b);
    printf("character is: %c",ch);

    return 0;
}