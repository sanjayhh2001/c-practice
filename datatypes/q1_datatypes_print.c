//Declare one int, float, char, and double variable, assign values, and print them.

#include <stdio.h>

int main()
{
    int a = 19; 
    float b = 3.145;
    char ch = 'S';
    double d = 3.158874;

    printf("The integer value is: %d\n",a);
    printf("The float value is: %f\n",b);
    printf("The character value is: %c\n",ch);
    printf("The double value is: %lf\n",d);
    
    return 0;
}